úspěšně jste prošli procesem ověření účtu investora. To znamená, že jste doložili svůj oprávněný zájem o zobrazení plného znění projektů.

Nyní můžete využívat všechny funkce portálu v roli investora – zejména podávat nabídky u projektů.

Upozorňujeme, že ověření účtu investora nemusí být podmínkou dostatečnou pro zobrazení plného znění projektů. Nabízející mohou u svého projektu zvolit vyšší stupeň ověření investorů a nastavit vlastní podmínky pro zpřístupnění všech informací – například podpis NDA. Ověření účtu investora je však podmínka nutná, abyste mohli nabízejícího o více informací požádat.

Pokud budete chtít učinit nabídku, musíte prokázat vážnost svého zájmu a složit jistotu ve výši, která je u jednotlivých projektů zveřejněna. V případě, že bude vaše nabídka vítězná, budete vyzváni k podpisu rezervační smlouvy a následné úhradě rezervačního poplatku do advokátní či notářské úschovy. K převodu projektu dojde až po podpisu kupní smlouvy. Celý proces zobchodování je detailně popsán ve [[Všeobecných obchodních podmínkách, {{ route('vseobecne-obchodni-podminky') }}]].

